package java_program;

public class Cond_elseif {
public static void main (String args[]) {
	int a = 50;
	if(a==20) {
		System.out.println("a value is 20");
	}
	else if(a==30) {
		System.out.println("a value is 30");
	}
	else if (a==40) {
		System.out.println("a value is 40");
	}
	else {
		System.out.println("this statment has been executed");
	}
}
}
